import type { HoleId, HoleState } from "../model/types";

export interface FingerPose {
  /** Finger-pad center. Closed poses match the target hole exactly. */
  readonly x: number;
  readonly y: number;
  /** Overall rotation around the finger pad, in degrees. */
  readonly rotate: number;
  readonly scaleX?: number;
  readonly scaleY?: number;
  readonly opacity?: number;
  /** Small, independently tuned joint rotations used by the SVG rig. */
  readonly proximalRotate: number;
  readonly middleRotate: number;
  readonly distalRotate: number;
}

